"""
Generate figures + CSV for the Jan 2026 PolyStore econ modeling draft.

Usage:
  python -m pip install -r requirements.txt
  python run_all.py

Outputs:
  outputs/market_dynamics_equilibrium_scenarios.csv
  outputs/*.png

Author: generated by GPT-5.2 Pro
"""
from __future__ import annotations

import os
from pathlib import Path
import math

import pandas as pd
import matplotlib.pyplot as plt

from model import (
    EmissionSchedule, GrowthScenario, CostCurve, MarketAssumptions,
    simulate_scenarios, PIB_TO_GIB, issuance_per_gibmonth_nil,
    apply_demand_shock, apply_cost_shock, equilibrium_token_price_usd, monthly_issuance_nil
)

OUT_DIR = Path(__file__).resolve().parent.parent / "outputs"
OUT_DIR.mkdir(parents=True, exist_ok=True)

def savefig(path: Path) -> None:
    plt.tight_layout()
    plt.savefig(path, dpi=200)
    plt.close()

def main() -> None:
    months = list(range(0, 121))  # 10 years monthly

    # Default: bootstrap-heavy bps decaying to bounded tail (matching included CSV/images)
    emission = EmissionSchedule(start_bps=425.0, tail_bps=25.0, halving_months=12.0)

    # Growth scenarios (logistic), caps are illustrative
    scenarios = [
        GrowthScenario(name="Slow (10 PiB cap)", cap_pib=10.0, k=0.10, t0=48.0),
        GrowthScenario(name="Base (100 PiB cap)", cap_pib=100.0, k=0.15, t0=36.0),
        GrowthScenario(name="Aggressive (1 EiB cap)", cap_pib=1000.0, k=0.18, t0=30.0),
    ]

    # Cost curve: $/GiB-month ~ cost_at_1pib * active_pib^-0.1
    cost_curve = CostCurve(cost_at_1pib_usd_per_gibmonth=0.007924556154438645, alpha=0.1)

    # Normalize storage fee to 1 NIL/GiB-month for readability
    market = MarketAssumptions(storage_price_nil_per_gibmonth=1.0)

    rows = simulate_scenarios(months, scenarios, emission, cost_curve, market)
    df = pd.DataFrame(rows)
    df.to_csv(OUT_DIR / "market_dynamics_equilibrium_scenarios.csv", index=False)

    # --- Figure: active storage growth ---
    plt.figure()
    for name, g in df.groupby("scenario"):
        plt.plot(g["month"], g["active_pib"], label=name)
    plt.xlabel("Month")
    plt.ylabel("Active storage (PiB)")
    plt.title("Active storage growth scenarios (illustrative)")
    plt.legend()
    savefig(OUT_DIR / "market_active_bytes_scenarios.png")

    # --- Figure: storage price in USD and cost curve ---
    plt.figure()
    for name, g in df.groupby("scenario"):
        plt.plot(g["month"], g["storage_price_usd_per_gibmonth"], label=f"Storage price (USD) — {name}")
        plt.plot(g["month"], g["cost_usd_per_gibmonth"], linestyle="--", label=f"Cost (USD) — {name}")
    plt.xlabel("Month")
    plt.ylabel("USD per GiB-month")
    plt.title("Implied storage price (USD) under cost-of-service mapping")
    plt.legend(fontsize=7)
    savefig(OUT_DIR / "market_storage_price_usd_scenarios.png")

    # --- Figure: token price in USD ---
    plt.figure()
    for name, g in df.groupby("scenario"):
        plt.plot(g["month"], g["token_price_usd"], label=name)
    plt.xlabel("Month")
    plt.ylabel("Token price (USD per NIL)")
    plt.title("Implied NIL token price (USD) under cost-of-service mapping")
    plt.legend()
    savefig(OUT_DIR / "market_token_price_usd_scenarios.png")

    # --- Figure: monthly issuance in NIL ---
    plt.figure()
    for name, g in df.groupby("scenario"):
        plt.plot(g["month"], g["issuance_monthly_nil"], label=name)
    plt.xlabel("Month")
    plt.ylabel("Monthly issuance (NIL)")
    plt.title("Base reward issuance (data-scaled) — monthly")
    plt.legend()
    savefig(OUT_DIR / "market_monthly_issuance_scenarios.png")

    # --- Cost sensitivity (vary cost_at_1pib) ---
    base_cost = cost_curve.cost_at_1pib_usd_per_gibmonth
    multipliers = [0.5, 0.75, 1.0, 1.25, 1.5]
    focus = df[df["scenario"]=="Base (100 PiB cap)"].copy()

    # Use month 36 snapshot (mid-growth) for sensitivity curves
    m_snap = 36
    focus_m = focus[focus["month"]==m_snap].iloc[0]
    active_pib = float(focus_m["active_pib"])
    bps = float(focus_m["bps"])

    storage_prices = []
    token_prices = []
    for mult in multipliers:
        cc = CostCurve(cost_at_1pib_usd_per_gibmonth=base_cost * mult, alpha=0.1)
        cost = cc.cost_usd_per_gibmonth(active_pib)
        token_p = equilibrium_token_price_usd(cost, market.storage_price_nil_per_gibmonth, bps)
        storage_usd = market.storage_price_nil_per_gibmonth * token_p
        token_prices.append(token_p)
        storage_prices.append(storage_usd)

    plt.figure()
    plt.plot(multipliers, storage_prices, marker="o")
    plt.xlabel("Cost multiplier (vs baseline)")
    plt.ylabel("USD per GiB-month")
    plt.title(f"Storage price sensitivity (month {m_snap}, Base scenario)")
    savefig(OUT_DIR / "market_storage_usd_cost_sensitivity.png")

    plt.figure()
    plt.plot(multipliers, token_prices, marker="o")
    plt.xlabel("Cost multiplier (vs baseline)")
    plt.ylabel("USD per NIL")
    plt.title(f"Token price sensitivity (month {m_snap}, Base scenario)")
    savefig(OUT_DIR / "market_token_price_cost_sensitivity.png")

    # --- Simple black swan shock (demand shock + cost shock) ---
    shock_month = 48
    drop_fraction = 0.35           # active storage drops 35%
    cost_mult = 1.6                # costs spike +60%
    recovery_hl = 6.0
    cost_decay_hl = 9.0

    base_s = GrowthScenario(name="Shock baseline (Base 100 PiB)", cap_pib=100.0, k=0.15, t0=36.0)
    active_base = [base_s.active_pib(m) for m in months]
    active_shock = apply_demand_shock(active_base, shock_month=shock_month, drop_fraction=drop_fraction, recovery_half_life_months=recovery_hl)

    # recompute time series under shocks
    bps_series = [emission.bps(m) for m in months]
    cost1_series = [apply_cost_shock(base_cost, m, shock_month=shock_month, shock_multiplier=cost_mult, decay_half_life_months=cost_decay_hl) for m in months]

    storage_usd = []
    token_usd = []
    issuance_nil = []
    profit_margin = []  # (revenue - cost)/cost per GiB-month
    for m in months:
        cc = CostCurve(cost_at_1pib_usd_per_gibmonth=cost1_series[m], alpha=0.1)
        cost = cc.cost_usd_per_gibmonth(active_shock[m])
        token_p = equilibrium_token_price_usd(cost, market.storage_price_nil_per_gibmonth, bps_series[m])
        storage_p = market.storage_price_nil_per_gibmonth * token_p
        issuance = monthly_issuance_nil(active_shock[m], market.storage_price_nil_per_gibmonth, bps_series[m])

        # Provider revenue per GiB-month (USD) from fees + issuance
        revenue_usd_per_gibmonth = token_p * market.storage_price_nil_per_gibmonth * (1.0 + bps_series[m]/10_000.0)
        pm = (revenue_usd_per_gibmonth - cost) / cost

        storage_usd.append(storage_p)
        token_usd.append(token_p)
        issuance_nil.append(issuance)
        profit_margin.append(pm)

    plt.figure()
    plt.plot(months, active_base, label="Baseline")
    plt.plot(months, active_shock, label="With shock")
    plt.axvline(shock_month, linestyle="--")
    plt.xlabel("Month")
    plt.ylabel("Active storage (PiB)")
    plt.title("Black swan demand shock (illustrative)")
    plt.legend()
    savefig(OUT_DIR / "shock_active_bytes_simple.png")

    plt.figure()
    plt.plot(months, storage_usd, label="Storage price (USD)")
    plt.axvline(shock_month, linestyle="--")
    plt.xlabel("Month")
    plt.ylabel("USD per GiB-month")
    plt.title("Black swan: implied storage price response (illustrative)")
    plt.legend()
    savefig(OUT_DIR / "shock_storage_usd_price_simple.png")

    plt.figure()
    plt.plot(months, issuance_nil, label="Monthly issuance (NIL)")
    plt.axvline(shock_month, linestyle="--")
    plt.xlabel("Month")
    plt.ylabel("NIL")
    plt.title("Black swan: monthly issuance response (data-scaled)")
    plt.legend()
    savefig(OUT_DIR / "shock_monthly_issuance_simple.png")

    plt.figure()
    plt.plot(months, profit_margin, label="Provider profitability proxy")
    plt.axhline(0.0, linestyle="--")
    plt.axvline(shock_month, linestyle="--")
    plt.xlabel("Month")
    plt.ylabel("(Revenue - Cost) / Cost")
    plt.title("Black swan: provider profitability proxy (illustrative)")
    plt.legend()
    savefig(OUT_DIR / "shock_provider_profitability_simple.png")

    # --- Emission curve visualization (monthly issuance vs active bytes; cumulative issuance) ---
    # Using a grid of active bytes and months
    active_pib_grid = np.logspace(-2, 3, 200)  # 0.01 PiB to 1000 PiB
    months_grid = [0, 12, 24, 36, 60, 120]
    plt.figure()
    for m in months_grid:
        bps_m = emission.bps(m)
        issuance = [monthly_issuance_nil(a, market.storage_price_nil_per_gibmonth, bps_m) for a in active_pib_grid]
        plt.plot(active_pib_grid, issuance, label=f"month {m} (bps={bps_m:.1f})")
    plt.xscale("log")
    plt.yscale("log")
    plt.xlabel("Active storage (PiB, log scale)")
    plt.ylabel("Monthly issuance (NIL, log scale)")
    plt.title("Base reward issuance vs active storage (selected months)")
    plt.legend(fontsize=7)
    savefig(OUT_DIR / "emission_monthly_vs_bytes.png")

    # Cumulative issuance for the base scenario
    base_df = df[df["scenario"]=="Base (100 PiB cap)"].copy()
    base_df["issuance_cum_nil"] = base_df["issuance_monthly_nil"].cumsum()
    plt.figure()
    plt.plot(base_df["month"], base_df["issuance_cum_nil"])
    plt.xlabel("Month")
    plt.ylabel("Cumulative issuance (NIL)")
    plt.title("Cumulative base reward issuance (Base scenario)")
    savefig(OUT_DIR / "emission_cumulative.png")

if __name__ == "__main__":
    import numpy as np
    main()
